public class ForLoopBase {
	public static void main(String[] args) {
		/**
		 * 1 2 10
		 */
		int a = 0;
		a = 1;
		System.out.println(a);
		a +=2;///java에서는 흔히 쓰인다 --> a++ => a=a+1
		System.out.println(a);
		a ++;
		System.out.println(a);
		a +=2;
		System.out.println(a);
		a +=2;
		System.out.println(a);

	}
}
